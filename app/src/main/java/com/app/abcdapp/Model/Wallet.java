package com.app.abcdapp.Model;

public class Wallet {
    String id,title,amount,date,time;
    public Wallet(){

    }

    public Wallet(String id, String title, String amount, String date, String time) {
        this.id = id;
        this.title = title;
        this.amount = amount;
        this.date = date;
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
